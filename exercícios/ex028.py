"""
EX028 - Faça a leitura de três valores e apresente como resultado a soma dos quadrados dos três valores lidos.
"""
vl1 = int(input('Digite o primeiro número: '))
vl2 = int(input('Digite o segundo número: '))
vl3 = int(input('Digite o terceiro número: '))

soma_quadrados = (vl1 ** 2) + (vl2 ** 2) + (vl3 ** 3)
print(soma_quadrados)